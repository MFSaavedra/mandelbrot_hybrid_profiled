#!/usr/bin/env python3
"""Plot the DLT retrodiction for report 28.

Reads sweep.csv (DLTlib SolveImage optimum vs per-frame collection cost l,
regenerated by run.sh). The fixed-partition curve and the measured points are
computed/copied from the report-27 data (experiments/25-frame-distribution/:
results_dynamic_yeco_scp.csv and results_dynamic_yeco.csv, weighted_static
and dyn_14 rows). Output: reports/img/dlt28_model.png (gitignored).
"""
import csv
import sys
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt

HERE = Path(__file__).resolve().parent
OUT = HERE.parents[1] / "reports" / "img" / "dlt28_model.png"

P_LAPTOP, P_YECO, FIXED = 0.2102, 0.0515, 1.2
L_TAR, L_SCP = 0.0122, 0.0835

ls, ny, wall = [], [], []
with open(HERE / "sweep.csv") as f:
    for r in csv.DictReader(f):
        ls.append(float(r["l_s_per_frame"]) * 1000)          # -> ms/frame
        ny.append(float(r["yeco_frames"]))
        wall.append(float(r["compute_wall_s"]) + FIXED)

# our static weights 1:4 (21/79), evaluated under the same model
fixed14 = [max(21 * P_LAPTOP, 79 * (P_YECO + l / 1000)) + FIXED for l in ls]

fig, (axa, axb) = plt.subplots(1, 2, figsize=(11.5, 4.6))

# ---- panel (a): optimal yeco share vs collection cost ----
axa.plot(ls, ny, color="#1f3a73", lw=2, zorder=2)
axa.axhline(79, color="#666666", ls="--", lw=1.4, zorder=1)
axa.text(97, 79.6, "our 1:4 weights (79 frames)", color="#666666",
         fontsize=9, ha="right")
for l, c, tag, dy in ((0.0, "#1f3a73", "compute-only: 80.3", 1.2),
                      (L_TAR * 1000, "#2E8B57", "tar: 76.7", 1.2),
                      (L_SCP * 1000, "#C00000", "scp: 60.9", -2.8)):
    i = min(range(len(ls)), key=lambda k: abs(ls[k] - l))
    axa.plot(ls[i], ny[i], "o", ms=8, color=c, zorder=3)
    axa.annotate(tag, (ls[i], ny[i]), (ls[i] + 2.5, ny[i] + dy),
                 color=c, fontsize=9)
axa.set_xlabel("per-frame collection cost $l$ (ms/frame)")
axa.set_ylabel("yeco's optimal share (frames of 100)")
axa.set_title("(a) DLT optimum share vs collection cost\n"
              "share $\\propto 1/(p_i+l_i)$; our weights used $1/p_i$ ($l$=0)")
axa.grid(alpha=0.3)

# ---- panel (b): predicted e2e vs collection cost, with measured points ----
axb.plot(ls, wall, color="#1f3a73", lw=2, label="DLT-optimal shares", zorder=2)
axb.plot(ls, fixed14, color="#666666", ls="--", lw=1.6,
         label="our 1:4 weights (fixed)", zorder=2)
# measured weighted-static e2e (results_dynamic_yeco{_scp,}.csv, 3 reps)
axb.errorbar([L_SCP * 1000], [13.11], yerr=[1.81], fmt="s", ms=8,
             color="#C00000", capsize=4, zorder=3,
             label="measured, weighted static")
axb.errorbar([L_TAR * 1000], [6.45], yerr=[0.26], fmt="s", ms=8,
             color="#2E8B57", capsize=4, zorder=3)
# measured dynamic 1:4 under scp: the stealer moved 21/79 -> 42/58
# (drawn at l+4ms only so the marker doesn't sit on the static square)
axb.errorbar([L_SCP * 1000 + 4], [12.95], yerr=[3.45], fmt="D", ms=7,
             mfc="white", color="#C00000", capsize=4, zorder=3,
             label="measured, dynamic (steals to 42/58)")
axb.annotate("", xy=(L_SCP * 1000 + 8, 10.0), xytext=(L_SCP * 1000 + 8, 12.4),
             arrowprops=dict(arrowstyle="->", color="#C00000"))
axb.text(L_SCP * 1000 - 3, 8.9, "stealing walks\ntoward the optimum",
         color="#C00000", fontsize=8, ha="right")
axb.set_xlabel("per-frame collection cost $l$ (ms/frame)")
axb.set_ylabel("end-to-end wall (s, incl. 1.2 s fixed)")
axb.set_title("(b) predicted e2e vs measured\n"
              "batching moves along the x-axis; re-weighting moves between curves")
axb.legend(fontsize=8, loc="upper left")
axb.grid(alpha=0.3)

fig.suptitle("DLTlib SolveImage ($a$=0, $c$=1) on the laptop+yeco pair: "
             "$p_\\ell$=210, $p_y$=51.5 ms/frame, $L$=100 frames",
             fontsize=10, y=1.0)
fig.tight_layout()
OUT.parent.mkdir(parents=True, exist_ok=True)
fig.savefig(OUT, dpi=160, bbox_inches="tight")
print(f"wrote {OUT}", file=sys.stderr)
